class TriFilter(name: String, val id: String) : Filter.TriState(name)

class CheckFilter(name: String, val id: String) : Filter.CheckBox(name)

abstract class TextFilter(name: String) : Filter.Text(name)

abstract class TriFilterGroup(
    name: String,
    options: List<Pair<String, String>>,
) : Filter.Group<TriFilter>(
    name,
    options.map { TriFilter(it.first, it.second) },
) {
    val included get() = state.filter { it.isIncluded() }.map { it.id }

    val excluded get() = state.filter { it.isExcluded() }.map { it.id }
}

abstract class SelectFilterGroup(
    name: String,
    private val options: List<Pair<String, String>>,
    defaultValue: String? = null,
) : Filter.Select<String>(
    name,
    options.map { it.first }.toTypedArray(),
    options.indexOfFirst { it.second == defaultValue }.takeIf { it != -1 } ?: 0,
) {
    val selected get() = options[state].second
}

abstract class CheckFilterGroup(
    name: String,
    private val options: List<Pair<String, String>>,
) : Filter.Group<CheckFilter>(
    name,
    options.map { CheckFilter(it.first, it.second) },
) {
    val checked get() = state.filter { it.state }.map { it.id }
}

class SortFilter(default: String? = null) : SelectFilterGroup(
    "Sort",
    sortList,
    default,
)

class GenreFilter : CheckFilterGroup(
    "Genre",
    genreList,
)

class TagFilter : TriFilterGroup(
    "Tags",
    tagList,
)

class YearFilter : TextFilter("Release Year") {
    val year get() = state.trim().toIntOrNull()?.toString()
}

class YearFilterMode : SelectFilterGroup(
    "Year Include Mode",
    listOf(
        Pair("Before", "before"),
        Pair("In", "in"),
        Pair("After", "after"),
    ),
    "in",
)

abstract class PageFilter(name: String) : TextFilter(name) {
    val page get() = state.trim().toIntOrNull()
}

class MaxPageFilter : PageFilter("Maximum Pages")

class MinPageFilter : PageFilter("Minimum Pages")

class UncensoredFilter : Filter.CheckBox("Uncensored Only")

